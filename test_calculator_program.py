import pytest
from calculate.calculator_program import calculate

def test_calculate_addition():
    assert calculate(1, 1, '+') == 2

def test_calculate_division():
    assert calculate(8, 2, '/') == 4

def test_calculate_unknown_operation():
    assert calculate(5, 5, 'unknown') == "Неизвестная операция."


# Новый тест для вычитания
def test_calculate_subtraction():
    assert calculate(10, 3, '-') == 7

# Новый тест для умножения
def test_calculate_multiplication():
    assert calculate(4, 5, '*') == 20

# Дополнительные полезные тесты
def test_calculate_division_by_zero():
    # Проверяем, что деление на 0 корректно обрабатывается
    assert calculate(5, 0, '/') == "На ноль делить нельзя."

def test_calculate_with_floats():
    # Проверяем работу с числами с плавающей точкой
    assert calculate(2.5, 1.5, '+') == 4.0
    assert calculate(3.0, 2.0, '*') == 6.0
    
